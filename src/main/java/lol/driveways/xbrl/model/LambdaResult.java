package lol.driveways.xbrl.model;

public class LambdaResult {

    private Integer cik;
    private String name;
    private Float score;

    public Integer getCik() {
        return cik;
    }

    public void setCik(Integer cik) {
        this.cik = cik;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getScore() {
        return score;
    }

    public void setScore(Float score) {
        this.score = score;
    }
}
